package org.gisoper.com.vo;

public class VehiclePosition {
    private String vehiclenum ;
    private String vehiclephone ;
    private String vahiclelat ;
    private String vahiclelng ;
    private String vahicletime ;

    public String getVehiclenum() {
        return vehiclenum;
    }

    public void setVehiclenum(String vehiclenum) {
        this.vehiclenum = vehiclenum;
    }

    public String getVehiclephone() {
        return vehiclephone;
    }

    public void setVehiclephone(String vehiclephone) {
        this.vehiclephone = vehiclephone;
    }

    public String getVahiclelat() {
        return vahiclelat;
    }

    public void setVahiclelat(String vahiclelat) {
        this.vahiclelat = vahiclelat;
    }

    public String getVahiclelng() {
        return vahiclelng;
    }

    public void setVahiclelng(String vahiclelng) {
        this.vahiclelng = vahiclelng;
    }

    public String getVahicletime() {
        return vahicletime;
    }

    public void setVahicletime(String vahicletime) {
        this.vahicletime = vahicletime;
    }
}
