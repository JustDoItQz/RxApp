package org.gisoper.com.vo;

public class WebsitePointVo {

    private String id ;
    private String lat ;
    private String lng ;
    private int websiteType ;
    private String lat2 ;
    private String lng2 ;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }

    public int getWebsiteType() {
        return websiteType;
    }

    public void setWebsiteType(int websiteType) {
        this.websiteType = websiteType;
    }

    public String getLat2() {
        return lat2;
    }

    public void setLat2(String lat2) {
        this.lat2 = lat2;
    }

    public String getLng2() {
        return lng2;
    }

    public void setLng2(String lng2) {
        this.lng2 = lng2;
    }
}
